var uf=["SP","RJ","AC","TO","MG"];
document.write("<br>"+uf+"<hr>");
uf.sort();//Faz o papel de ordenar ou classificar em ordem crescente de A a Z, ordem alfabetica
document.write(uf+"<hr>");
uf.reverse();// Pega o valor original e reverte a ordem.
// para deixar na forma decrecente de Z-A teria que utilizar antes o sort.
// para utilizar com numeros teria que utilizar outra função junto.
document.write(uf+"<hr>");